# Programa para converter Kilometros para Milhas 
# Conversor de km para milhas é 1 Km = 0.621371
print('\nConversor de KM para Milhas!')

km = float(input('Digite a distancia em KM que precisa ser convertida: '))
conversor = 0.621371
milhas = km * conversor

print(f'A distancia {km} KM é igual a {milhas}')



